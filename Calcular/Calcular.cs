public class Calcular
{
    
    public double Soma(double n1, double n2)//oi
    {
        return n1 + n2;
    }

    public double Subtracao(double n3, double n4)
    {
        return n3 - n4;
    }

    public double Multiplicacao(double n5, double n6)
    {
        return n5 * n6;
    }

    public double divisao(double n7, double n8)
    {
        return n7 / n8;
    }
}
